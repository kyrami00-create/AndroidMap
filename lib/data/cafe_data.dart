import 'package:latlong2/latlong.dart';

class CafePoint {
  final String name;
  final LatLng position;
  final String type;
  final String building;
  final String floor;
  final String description;
  final int placesCount;
  final String imageUrl;
  final String workingHours;
  final String capacity;
  final bool wifi;
  final bool charging;
  int clusterId;

  CafePoint({
    required this.name,
    required this.position,
    required this.type,
    this.building = '',
    this.floor = '',
    this.description = '',
    this.placesCount = 1,
    this.imageUrl = '',
    this.workingHours = '',
    this.capacity = '',
    this.wifi = false,
    this.charging = false,
    this.clusterId = -1,
  });

  CafePoint copyWith({int? clusterId}) => CafePoint(
    name: name,
    position: position,
    type: type,
    building: building,
    floor: floor,
    description: description,
    placesCount: placesCount,
    imageUrl: imageUrl,
    workingHours: workingHours,
    capacity: capacity,
    wifi: wifi,
    charging: charging,
    clusterId: clusterId ?? this.clusterId,
  );
}

